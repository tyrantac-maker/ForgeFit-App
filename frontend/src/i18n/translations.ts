export type TranslationKey = keyof typeof UI_TRANSLATIONS.en;

export function getUIText(langCode: string, key: TranslationKey, params?: Record<string, string | number>): string {
  const base = langCode?.split('-')[0] || 'en';
  const exact = UI_TRANSLATIONS[langCode];
  const fallbackLang = exact || UI_TRANSLATIONS[base] || UI_TRANSLATIONS['en'];
  let text = fallbackLang[key] || UI_TRANSLATIONS['en'][key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{{${k}}}`, String(v));
    });
  }
  return text;
}
